      DO 30 I=1,MR2
      DUM = A(I,KK)
      A(I,KK) = -B(I)
      B(I) = -DUM/CC(1)
C---- NOW MODIFY (K+1) COL OF [A]
      A(I,KK+1) = A(I,KK+1) - DUM*CC(2)/CC(1)
30    CONTINUE
      ELSE
C---- FIRST SWITCH (K+1)TH COL OF [A] AND RHS VECTOR B
      DO 35 I=1,MR2
      DUM = A(I,KK+1)
      A(I,KK+1) = -B(I)
      B(I) = -DUM/CC(2)
C---- NOW MODIFY KTH COL OF [A]
      A(I,KK) = A(I,KK) - DUM*CC(1)/CC(2)
35    CONTINUE
      END IF
C---- NOW GET TOTAL RHS VECTOR
      MR22 = MR + 2
      IF(MR22.EQ.MR2) THEN
      DO 40 I = 1,MR2
      B(I) = B(I)*DELPROP + C(I)
40    CONTINUE
      ELSE
      DO 42 I =1,MR2
      B(I) = B(I)*DELPROP
42    CONTINUE
      END IF 
