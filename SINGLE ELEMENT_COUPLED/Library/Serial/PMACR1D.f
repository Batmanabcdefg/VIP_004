      SUBROUTINE RITZSB6(U,DELD,NDF,MSET,MR,MRVAR,DELPROP)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C---- AUTO CONTROL OF RITZ VARIABLE FOR STABILIZATION 
C---- IDENTIFY VARIABLE TO BE STABILIZED BASED ON WHICH ONE HAD THE
C---- FASTEST GROWTH RATE IN THE PREVIOUS TIME STEP. PRESCRIBE THE SAME
C---- DISPL INCREMENT AS IN THE PREV STEP FOR THAT VARIABLE
