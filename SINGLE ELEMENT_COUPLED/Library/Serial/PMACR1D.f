      SUBROUTINE RITZSB3(EK,F,R,DF,RI,JDIAG,A,B,C,NEQ,MR,MR2)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z) 
C---- FORM REDUCED STIFF MATRIX AND RHS VECTORS
C
      DIMENSION EK(*),F(*),R(*),RI(NEQ,*),A(MR2,*),B(*),C(*),JDIAG(*)
     1 ,DF(*)
C---- NOTE MR2 = MRITZ+2. IF OLDFRC = 0, MR2 = MRITZ + 1
C----  FIRST FORM REDUCED RHS VECTORS
      DO 10 I=1,MR2
      DO 5 J=1,NEQ
      DF(J) = RI(J,I)
5     CONTINUE
      B(I) = DOT(DF,F,NEQ)
      C(I) = DOT(DF,R,NEQ)
10    CONTINUE
C---- NOW FORM REDUCED STIFF MATRIX
      DO 20 J = 1,MR2
      CALL PZERO(DF,NEQ)
      CALL PROMUL(EK,RI(1,J),DF,JDIAG,NEQ)
      DO 30 I=1,MR2
      A(I,J) = DOT(RI(1,I),DF,NEQ)
30    CONTINUE
20    CONTINUE
      RETURN
      END
C
      SUBROUTINE RITZSB4(A,B,C,DELD,DELPROP,MR,MR2,MRVAR,FLAG)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C---- SOLVE REDUCED SYSTEM OF EQNS
C
      LOGICAL FLAG
      DIMENSION A(MR2,*),B(*),C(*),DELD(*)
      MR22 = MR + 2
      IF(FLAG) THEN
C---- THE VARIABLE # MRVAR HAS BEEN SPECIFIED FOR SPECIAL CONTROL
C---- FIRST SWITCH COL # MRVAR AND RHS VECTOR B(I)
      DO 20 I = 1,MR2
      DUM = A(I,MRVAR)
      A(I,MRVAR) = -B(I)
      B(I) = -DUM
20    CONTINUE
      END IF
      IF(MR22.EQ.MR2) THEN
      DO 10 I = 1,MR2
      B(I) = B(I)*DELPROP + C(I)
10    CONTINUE
      ELSE
      DO 12 I =1,MR2
      B(I) = B(I)*DELPROP
12    CONTINUE
      END IF 
      CALL SOLVE(A,B,MR2,1)
      DO 15 I=1,MR2
      DELD(I) = B(I)
15    CONTINUE
      IF(MR22.NE.MR2) DELD(MR22) = 0.D0
      IF(.NOT.FLAG) THEN
      DELD(MR22+1) = DELPROP
      ELSE
      DELD(MR22+1) = B(MRVAR)
      DELD(MRVAR) = DELPROP
      END IF
      RETURN
      END
C
      SUBROUTINE RITZSB5(DR,R,RI,DELD,PROP,PROPOL,MR,MR2,NEQ)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C---- RECONSTRUCT GLOBAL NODAL DISPL VECTOR
C
      DIMENSION DR(*),RI(NEQ,*),DELD(*),R(*),PROP(7),PROPOL(7)
C---- KEEP INITIAL OUT-OF-BALANCE FORCES FOR NORM CALCULATION
      MR22 = MR + 2
      DO 5 I = 1,NEQ
      R(I) = R(I) + DELD(MR22+1)*DR(I)
5     CONTINUE
C---- NOW COMPUTE INCR NODAL DISPL VECTOR
      CALL PZERO(DR,NEQ)
      DO 10 I =1,NEQ
      DO 15 J =1,MR2
      DR(I) = DR(I) + RI(I,J)*DELD(J)
15    CONTINUE
10    CONTINUE
      PROP(1) = PROPOL(1) + DELD(MR22+1)
      RETURN
      END 
