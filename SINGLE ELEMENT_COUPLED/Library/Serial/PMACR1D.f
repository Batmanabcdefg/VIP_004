      SUBROUTINE NEWMST(A,B,ID,DR,ACCELG,JDIAG,NEQ)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
C
C----  MODIFIES STIFFNESS MATRIX, & FORCE VECTOR BY ADDING CONTRBN FROM
C----  MASS MATRIX WHEN DOING NEWMARK -BETA, GAMMA INTEGRN
C---- 'A' IS STIFFNESS MATRIX AND 'B' IS LUMPED MASS MATRIX
C
      DIMENSION A(*),B(*),ID(*),JDIAG(*),DR(*),ACCELG(*)
      COMMON /TDATA/ TIME,DT,BETA,GAMMA
      DO 10 I=1,NEQ
      IF(ID(I).EQ.0) THEN
      JJ = JDIAG(I)
      A(JJ) = A(JJ) + B(I)/(DT*DT*BETA)
      DR(I) = DR(I) - B(I)*ACCELG(I)
      END IF
10    CONTINUE
      RETURN
      END
