     1 XLNTND(100),ANGINT(100), CPINT(2,100),
     2 XLN(100), ISTAT(100)
      COMMON /TDATA/ TIME,DT,BETA,GAMMA
      DIMENSION A(MR2,*),B(*),C(*),DELD(*),U(NDF,*),ID(NDF,*)
      DIMENSION CC(2)
      DATA TOL /1.0D-02/
C---- CALCULATE UND FOR NODE TO BE STABILIZED
      DELNC=DLTAA
      L =INTND(1,IMAX)
C____ L is global intf node referred to material 1
C---- FORM CONSTRAINT EQN BASED ON PRESCRIBING LAMDADOT FOR ABOVE NODE
      CC(1) = -DSIN(ANGINT(IMAX))/DELNC
      CC(2) = DCOS(ANGINT(IMAX))/DELNC
C---- COLS OF [A] TO BE ALTERED ARE KK = 1,2
      KK = 1
      IF((DABS(CC(1)).LT.1.0D-12).AND.(DABS(CC(2)).LT.1.0D-12)) THEN
      WRITE(6,2001)
