      COMMON /PRTSET/ NSET(9,201)
      DATA TOL /1.0D-08/
      MR1 = MR/NDF
      DISPNR = 0.D0
      DO 10 I=1,MR1
      N = NSET(MSET,I+1)
      DO 15 J=1,NDF
      IJ = (I-1)*NDF + J
      NJ = (N-1)*NDF + J
      TOTDSP = U(NJ)
      IF(DABS(TOTDSP).LE.TOL) GO TO 15
      VAR = DABS(DELD(IJ)/TOTDSP)
      IF(VAR.GT.DISPNR) THEN
      DISPNR = VAR
      MRVAR = IJ
      DELPROP = DELD(IJ)
      END IF
15    CONTINUE
10    CONTINUE
      RETURN
      END
C
      SUBROUTINE RITZSB71(A,B,C,DELD,U,ID,DELPROP,NDF,MR,MR2,IMAX)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C---- SPECIAL CONTROL OF RITZ VARIABLES FOR COHESIVE ZONE MODELS
C---- SOLVE REDUCED SYSTEM OF EQNS
C
      COMMON /COHES/ NINTND, INTND(2,100), ALPAA, DLTAA, SIGMXX,
     1 XLNTND(100),ANGINT(100), CPINT(2,100),
     2 XLN(100), ISTAT(100)
      COMMON /TDATA/ TIME,DT,BETA,GAMMA
      DIMENSION A(MR2,*),B(*),C(*),DELD(*),U(NDF,*),ID(NDF,*)
      DIMENSION Q(2,2),AA(2,4),QA(2,4),BB(2),CC(4)
      DATA TOL /1.0D-02/
C---- CALCULATE UTD,UND,ALAMDA FOR NODE TO BE STABILIZED
      ALMAX = 0.D0
      DELNC=DLTAA
      DELTC=DLTAA/ALPAA
      L =INTND(1,IMAX)
      L0=INTND(2,IMAX)
C____ L & L0 are global intf nodes referred to material 1 and 2
C____ For global node "L", form the rotation matx
      Q(1,1) =DCOS(ANGINT(IMAX))
      Q(1,2) =DSIN(ANGINT(IMAX))
      Q(2,1) = -Q(1,2)
      Q(2,2) = Q(1,1)
C____ Calculate UT and UN from relative displacemet UX and UY
C____ between nodes "L" and "L0".
      UT =Q(1,1)*(U(1,L)-U(1,L0))+Q(1,2)*(U(2,L)-U(2,L0))
      UN =Q(2,1)*(U(1,L)-U(1,L0))+Q(2,2)*(U(2,L)-U(2,L0))
      IF(UN.LT.0.D0) UN = 0.D0
      UTD =UT/DELTC
      UND =UN/DELNC
      ALAMDA=DSQRT(UTD*UTD+UND*UND)
      ALMAX = ALAMDA
      UTM = UT
      UNM = UN
      IF(ALMAX.EQ.0.D0) THEN   
      WRITE(6,2002)
2002  FORMAT(5X,'********ERROR IN INTERFACE STABILIZATION',/,
     1       5x,'LAMDA FOR INTF NODE TO BE STABILIZED IS ZERO')
      STOP
      END IF
C
C---- FORM CONSTRAINT EQN BASED ON PRESCRIBING LAMDADOT FOR ABOVE NODE-PAIR
      BB(1) = UTM/(DELTC*DELTC*ALMAX)
      BB(2) = UNM/(DELNC*DELNC*ALMAX)
      DO 15 I=1,2
      DO 15 J=1,4
      AA(I,J) = 0.D0
      QA(I,J) = 0.D0
15    CONTINUE
      AA(1,1) = 1.D0
      AA(1,3) = -1.D0
      AA(2,2) = 1.D0
      AA(2,4) = -1.D0
      Q(1,1) =DCOS(ANGINT(IMAX))
      Q(1,2) =DSIN(ANGINT(IMAX))
      Q(2,1) = -Q(1,2)
      Q(2,2) = Q(1,1)
      DO 20 I=1,2
      DO 20 J=1,4
      DO 20 K=1,2
      QA(I,J) = QA(I,J) + Q(I,K)*AA(K,J)
20    CONTINUE
      DO 25 I=1,4
      CC(I) = 0.D0
      DO 25 K=1,2
      CC(I) = CC(I) + BB(K)*QA(K,I)
25    CONTINUE
C
c----------------------------------------------------
C     write(6,3000) (bb(i),i=1,2)
3000  format(5x,'bb',2e13.5)
C     write(6,3001) (cc(i),i=1,4)
3001  format(5x,'cc',4e13.5)
c------------------------------------------------------------
C---- COLS OF [A] TO BE ALTERED ARE KK = 1,2,3,4
      KK = 1
      IF((DABS(CC(1)).LT.1.0D-12).AND.(DABS(CC(2)).LT.1.0D-12)) THEN
      WRITE(6,2001)
2001  FORMAT(5X,'ERROR IN CONTROLLING LAMDA. CC(1).AND.CC(2) = 0')
      STOP
      END IF
      IF(DABS(CC(1)).GT.DABS(CC(2))) THEN
      IOP = 1
      ELSE
      IOP = 2
      END IF
c----------------------------------------------------------------
C     write(6,3002) kk,iop,mr2
C3002  format(5x,'kk=',i3,2x,'iop=',i3,2x,'mr2=',i3,/,
C    1 5x,'A matrix before modification')
C     write(6,3003) ((a(i,j),i=1,mr2),j=1,mr2)
3003  format(9e13.5)
C     write(6,3004)
3004  format(5x,'B vector before modification')
C     write(6,3003) (b(i),i=1,mr2)
C     write(6,3005)
3005  format(5x,'C vector before modification')
C     write(6,3003) (c(i),i=1,mr2)
c-------------------------------------------------------------
      IF(IOP.EQ.1) THEN
C---- FIRST SWITCH KKTH COL OF [A] AND RHS VECTOR B
      DO 30 I=1,MR2
      DUM = A(I,KK)
      A(I,KK) = -B(I)
      B(I) = -DUM/CC(1)
C---- NOW MODIFY (K+1), (K+2) AND (K+3) COLS OF [A]
      A(I,KK+1) = A(I,KK+1) - DUM*CC(2)/CC(1)
      A(I,KK+2) = A(I,KK+2) - DUM*CC(3)/CC(1)
      A(I,KK+3) = A(I,KK+3) - DUM*CC(4)/CC(1)
30    CONTINUE
      ELSE
C---- FIRST SWITCH (K+1)TH COL OF [A] AND RHS VECTOR B
      DO 35 I=1,MR2
      DUM = A(I,KK+1)
      A(I,KK+1) = -B(I)
      B(I) = -DUM/CC(2)
C---- NOW MODIFY K, (K+2) AND (K+3) COLS OF [A]
      A(I,KK) = A(I,KK) - DUM*CC(1)/CC(2)
      A(I,KK+2) = A(I,KK+2) - DUM*CC(3)/CC(2)
      A(I,KK+3) = A(I,KK+3) - DUM*CC(4)/CC(2)
35    CONTINUE
      END IF
C---- NOW GET TOTAL RHS VECTOR
      MR22 = MR + 2
      IF(MR22.EQ.MR2) THEN
      DO 40 I = 1,MR2
      B(I) = B(I)*DELPROP + C(I)
40    CONTINUE
      ELSE
      DO 42 I =1,MR2
      B(I) = B(I)*DELPROP
42    CONTINUE
      END IF 
C
C---- NOW WE GOT TO MODIFY [A] BY ZEROING ROWS AND COLS (EXCEPT DIAG)
C---- CORRESP TO DOFS WITH FIXED DISPLS
      IF((ID(1,L).NE.0).AND.(IOP.EQ.2)) THEN 
      DO 50 IM=1,MR2
      A(IM,1) = 0.D0
      A(1,IM) = 0.D0
50    CONTINUE
      B(1) = 0.D0
      A(1,1) = 1.D0
      END IF
      IF((ID(2,L).NE.0).AND.(IOP.EQ.1)) THEN 
      DO 51 IM=1,MR2
      A(IM,2) = 0.D0
      A(2,IM) = 0.D0
51    CONTINUE
      B(2) = 0.D0
      A(2,2) = 1.D0
      END IF
      IF(ID(1,L0).NE.0) THEN 
      DO 52 IM=1,MR2
      A(IM,3) = 0.D0
      A(3,IM) = 0.D0
52    CONTINUE
      B(3) = 0.D0
      A(3,3) = 1.D0
      END IF
      IF(ID(2,L0).NE.0) THEN 
      DO 53 IM=1,MR2
      A(IM,4) = 0.D0
      A(4,IM) = 0.D0
53    CONTINUE
      B(4) = 0.D0
      A(4,4) = 1.D0
      END IF
C
C---- SOLVE FOR DELDOTS
c--------------------------------------------------------------
C     write(6,3006)
3006  format(5x,'A, B, C before entering SOLVE')
C     write(6,*) ((a(i,j),i=1,mr2),j=1,mr2)
C     write(6,*) (b(i),i=1,mr2)
C     write(6,*) (c(i),i=1,mr2)
c-----------------------------------------------------------
      CALL SOLVE(A,B,MR2,1)
      DO 60 I=1,MR2
      DELD(I) = B(I)
60    CONTINUE
      IF(MR22.NE.MR2) DELD(MR22) = 0.D0
c-----------------------------------------------------
C     write(6,3007)
3007  format(5x,'deld obtained from solve')
C     write(6,*) (deld(i),i=1,mr2)
c---------------------------------------------
C
C---- SWITCH BACK DELDOTS
      IF(IOP.EQ.1) THEN
      DELD(MR22+1) = B(KK)
      DELD(KK) = DELPROP - CC(2)*DELD(KK+1) - CC(3)*DELD(KK+2)
     1 - CC(4)*DELD(KK+3)
      DELD(KK) = DELD(KK)/CC(1)
      ELSE
      DELD(MR22+1) = B(KK+1)
      DELD(KK+1) = DELPROP - CC(1)*DELD(KK) - CC(3)*DELD(KK+2)
     1 - CC(4)*DELD(KK+3)
      DELD(KK+1) = DELD(KK+1)/CC(2)
      END IF 
c-----------------------------------------------------
C     write(6,3008)
3008  format(5x,'deld after modification')
C     write(6,3003) (deld(i),i=1,mr22+1)
c---------------------------------------------
      RETURN
      END
