      SUBROUTINE RITZSB1(A,F,R,AP,RI,JDIAG,NEQ,NDF,MR,MSET)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C---- MODIFY [K] MATRIX AND FORM MODIFIED RHS VECTORS
      DIMENSION A(*),F(*),R(*),AP(*),RI(NEQ,*),JDIAG(*)
      COMMON /PRTSET/ NSET(9,201)
      MR1 = MR/NDF
C---- FIRST TRANSFER A TO AP
      DO 5 I = 1,JDIAG(NEQ)
      AP(I) = A(I)
5     CONTINUE
C---- TRANSFER F AND R TO (MR+1) AND (MR+2) RHS VECTORS
      DO 6 I =1,NEQ
      RI(I,MR+1) = F(I)
      RI(I,MR+2) = R(I)
6     CONTINUE
C---- LOOP OVER ALL NODES WHOSE NODAL DISPLS HAVE TO BE CONTROLLED
C---- FORM THE RHS VECTORS CORRESP TO 1 TO MR BY TAKING THE -VE OF THE
C---- CORRESP COLS OF K MATRIX
C---- ALSO INSERT ZEROES IN APPROPRIATE COLS (AND 1 ON DIAGS) OF AP
      DO 10 I=1,MR1
      N = NSET(MSET,I+1)
      DO 15 J=1,NDF
      IJ = (I-1)*NDF + J
C---- IJ IS THE RHS VECTOR WHICH IS BEING FORMED
      NJ = (N-1)*NDF + J
C---- NJ IS COL NUMBER OF [K] WHICH WILL BE USED TO FORM THE ABOVE RHS VECTOR
C---- FIRST TREAT TERMS ON AND ABOVE THE DIAGONAL OF COLUMN NJ OF STIFF MATRIX
      IF(NJ.EQ.1) THEN
      NJ1D = 0
      ELSE
      NJ1D = JDIAG(NJ-1)
      END IF
      DO 20 K=1,NJ
      KNJ = JDIAG(NJ) - NJ + K
      IF(KNJ.LE.NJ1D) THEN
C---- THE TERM (K,NJ) IS OUTSIDE THE PROFILE OF [K] AND IS ALREADY ZERO
      RI(K,IJ) = 0.D0
      ELSE
      RI(K,IJ) = -A(KNJ)
C---- PUT ZERO IN THE CORRESP TERM OF AP (IF IT IS OFF-DIAG) AND UNITY IF
C---- ON DIAGONAL
      AP(KNJ) = 0.D0
      IF(K.EQ.NJ) AP(KNJ) = 1.D0
      END IF
20    CONTINUE
C---- NOW FOR TERMS BELOW THE DIAGONAL OF COLUMN NJ OF STIFF MATRIX
      DO 25 K=NJ+1,NEQ
C---- SINCE MATRIX IS SYMM K(K,NJ) = K(NJ,K) = A(JDIAG(K)-K+NJ)
      NJK = JDIAG(K) - K + NJ
      IF(NJK.LE.JDIAG(K-1)) THEN
C---- THE TERM (NJ,K) IS OUTSIDE THE PROFILE OF [K] AND IS ALREADY ZERO
      RI(K,IJ) = 0.D0
      ELSE
      RI(K,IJ) = -A(NJK)
C---- ZERO THE CORRESP TERM OF AP
      AP(NJK) = 0.D0
      END IF
25    CONTINUE
15    CONTINUE
10    CONTINUE
C---- FINALLY INSERT ZEROES IN APPROPRIATE ROWS OF ALL RHS VECTORS 
      DO 30 I=1,MR1
      N = NSET(MSET,I+1)
      DO 35 J=1,NDF
      IJ = (I-1)*NDF + J
      NJ = (N-1)*NDF + J
C---- NJ IS ROW OF RHS VECTORS CORRESP TO THE VARIABLE IJ WHICH IS BEING STABILIZED
C---- LOOP OVER ALL (1 TO MR) RHS VECTORS
      DO 40 K=1,MR
      IF(K.NE.IJ) THEN
      RI(NJ,K) = 0.D0
      ELSE
      RI(NJ,K) = 1.D0
      END IF
40    CONTINUE
      RI(NJ,MR+1) = 0.D0
      RI(NJ,MR+2) = 0.D0
35    CONTINUE
30    CONTINUE
      RETURN
      END
