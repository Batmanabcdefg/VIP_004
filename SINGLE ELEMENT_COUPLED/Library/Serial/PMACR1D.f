      SUBROUTINE CNDMAS(A,B,JDIAG,NEQ,IOP)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
C
C---- SETS UP DUMMY DIAGONAL MASS MATRIX FOR COMPUTING
C---- EIGENVALUES OF SCALED K MATRIX (IOP = 1) OR
C---- EIGENVALUES OF UNSCALED K MATRIX (IOP.NE.1)
C---- 'A' IS STIFFNESS MATRIX AND 'B' IS MASS MATRIX
C
      DIMENSION A(*),B(*),JDIAG(*)
      DO 10 I=1,NEQ
      JJ = JDIAG(I)
      IF(IOP.EQ.1) B(I) = A(JJ)
      IF(IOP.NE.1) B(I) = 1.D0
10    CONTINUE
      RETURN
      END
