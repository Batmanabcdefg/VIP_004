C---- CORRESP TO DOFS WITH FIXED DISPLS
      IF((ID(1,L).NE.0).AND.(IOP.EQ.2)) THEN 
      DO 50 IM=1,MR2
      A(IM,1) = 0.D0
      A(1,IM) = 0.D0
50    CONTINUE
      B(1) = 0.D0
      A(1,1) = 1.D0
      END IF
      IF((ID(2,L).NE.0).AND.(IOP.EQ.1)) THEN 
      DO 51 IM=1,MR2
      A(IM,2) = 0.D0
      A(2,IM) = 0.D0
51    CONTINUE
      B(2) = 0.D0
      A(2,2) = 1.D0
      END IF
C
C---- SOLVE FOR DELDOTS
      CALL SOLVE(A,B,MR2,1)
      DO 60 I=1,MR2
      DELD(I) = B(I)
60    CONTINUE
      IF(MR22.NE.MR2) DELD(MR22) = 0.D0
C
C---- SWITCH BACK DELDOTS
      IF(IOP.EQ.1) THEN
      DELD(MR22+1) = B(KK)
      DELD(KK) = DELPROP - CC(2)*DELD(KK+1)
      DELD(KK) = DELD(KK)/CC(1)
      ELSE
      DELD(MR22+1) = B(KK+1)
      DELD(KK+1) = DELPROP - CC(1)*DELD(KK)
      DELD(KK+1) = DELD(KK+1)/CC(2)
      END IF 
      RETURN
      END
